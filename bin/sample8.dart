void main(){
  //relational opertor
  print(100>90);
  print(60<80);
  print(100>=200);
  print(400<=300);
  print(300!=200);
  print("***************");
  //logical opertion
  int k=100;
  print(k<=10 && k>=50);
  print(k>=40 || k==50);
  print(!(k==100));
  print("****************");
  //bitwise operator
  int m=2; //0001
  int n=4;// 0100
  //m&n=1010
  //m|n=0101
  //m^n=0101
  print(m&n);
  print(m|n);
  print(m^n);
  print("*****************");
  //shift operator
  int r=4;
  print(r>>3);
  print(r<<4);  /// 0100 00000


}